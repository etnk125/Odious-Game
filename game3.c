        printf("\n\n\n");
        int i,j;
        for(i=0; i<23; i++)
        {
            printf("\t\t\t\t\t ");
            for(j=0; pacmap[i][j]!='\0'; j++)
            {
                if(pacmap[i][j]=='@')colour(violet,black);
                if(isalpha(pacmap[i][j]))colour(green,black);
                if(pacmap[i][j]=='$')
                {
                    colour(yellow,black);
                    printf("%c",2);
                }
                else
                    printf("%c",pacmap[i][j]);
                recolour();
            }
            printf("\n");

        }
        fflush(stdin);
        ch=getch();
        int x=0,y=0;

        bool mov=0;
        bool bot[5]= {0,0,0,0,0};
        for(i=0; i<23; i++)
        {
            for(j=0; pacmap[i][j]!='\0'; j++)
            {
                x=0;
                y=0;
                if(isalpha(pacmap[i][j]))
                {
                    if(!bot[pacmap[i][j]-'A'])
                    {
                        bot[pacmap[i][j]]=1;
                        do
                        {
                            x=0;
                            y=0;
                            int ran=rand()%4;
                            switch(ran)
                            {
                            case 0:
                                x=1;
                                break;
                            case 1:
                                x=-1;
                                break;
                            case 2:
                                y=1;
                                break;
                            case 3:
                                y=-1;
                                break;

                            }

                        }
                        while(pacmap[i+y][j+x]=='#'||isalpha(pacmap[i+y][j+x]));
                        if(pacmap[i+y][j+x]=='$')
                        {
                            win=0;
                            com=1;
                        }
                        pacmap[i+y][j+x]=pacmap[i][j];
                        pacmap[i][j]=' ';



                    }
                }
            }
        }
        if(com)
            break;
        x=0;
        y=0;
        switch(ch)
        {
        case 'w':
            y--;
            break;
        case 's':
            y++;
            break;
        case 'a':
            x--;
            break;
        case 'd':
            x++;
            break;
        }
        for(i=0; i<23; i++)
        {
            for(j=0; pacmap[i][j]!='\0'; j++)
            {
                if(pacmap[i][j]=='$'&&!mov)
                {
                    mov=1;
                    if(pacmap[i+y][j+x]=='#')
                        continue;
                    if(isalpha(pacmap[i+y][j+x]))
                    {
                        com=1;
                        win=0;
                        pacmap[i][j]=' ';
                    }
                    else if(pacmap[i+y][j+x]=='@')
                    {
                        com=1;
                        win=1;
                        pacmap[i][j]=' ';
                    }
                    else
                    {
                        pacmap[i][j]=' ';
                        pacmap[i+y][j+x]='$';

                    }

                }
            }

        }
