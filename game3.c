void password(char pass[])
{
    char ch;
    int i=0;
    bool com=0;
    while(!com)
    {
        ch=getch();
        switch(ch)
        {
        case 13:
        {
            pass[i]='\0';
            com=1;
            break;
        }
        case 8:
        {
            if(i>0)
            {
                i--;
                printf("\b \b");
            }
            break;
        }
        default:
        {
            if(ch!=32&&ch!=9)
            {
                printf("*");
                pass[i]=ch;
                i++;
            }
        }
        }
    }
}
